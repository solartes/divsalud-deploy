package com.unicauca.divsalud.sessionbeans;

import com.unicauca.divsalud.entidades.TipoIdentificacion;
import java.util.List;
import javax.ejb.Stateless;
import javax.persistence.EntityManager;
import javax.persistence.PersistenceContext;

/**
 *
 * @author ROED26
 */
@Stateless
public class TipoIdentificacionFacade extends AbstractFacade<TipoIdentificacion> {

    @PersistenceContext(unitName = "divisionsaludPU")
    private EntityManager em;

    @Override
    protected EntityManager getEntityManager() {
        return em;
    }

    public TipoIdentificacionFacade() {
        super(TipoIdentificacion.class);
    }
    
    public List<TipoIdentificacion> findByName(String name)
    {
        return em.createNamedQuery("TipoIdentificacion.findByName").setParameter("name", name).getResultList();
    }
    
}
